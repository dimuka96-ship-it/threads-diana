// JSON-схема ответа (подмножество OpenAPI для Gemini).
const SCHEMA = {
  type: "object",
  properties: {
    post: { type: "string", description: "Готовий текст поста для Threads українською, <= 500 символів" },
    char_count: { type: "integer", description: "Кількість символів у post" },
    needs_image: { type: "boolean", description: "Чи підсилить пост картинка" },
    image_idea: { type: "string", description: "Коротка ідея візуалу, якщо needs_image=true, інакше порожній рядок" },
    hashtags: { type: "array", items: { type: "string" }, description: "0-2 хештеги без #" },
  },
  required: ["post", "char_count", "needs_image", "image_idea", "hashtags"],
  propertyOrdering: ["post", "char_count", "needs_image", "image_idea", "hashtags"],
};

function apiKey() {
  return process.env.GEMINI_API_KEY || process.env.GOOGLE_API_KEY;
}

let ai = null;
async function getClient() {
  if (!apiKey()) return null;
  if (!ai) {
    const { GoogleGenAI } = await import("@google/genai");
    ai = new GoogleGenAI({ apiKey: apiKey() });
  }
  return ai;
}

export function aiAvailable() {
  return Boolean(apiKey());
}

// Генерирует один пост по брифу. Возвращает объект по SCHEMA или null при сбое.
export async function generatePost(brief, recents = []) {
  const c = await getClient();
  if (!c) return null;

  const lines = [
    `Тема поста: ${brief.themeTitle}`,
    `Кут подачі: ${brief.angle}`,
    `Для кого (сегмент ЦА): ${brief.segment}`,
    `КОНКРЕТНА тема, яку розкрити (не сповзай у загальне): ${brief.subtopic}`,
    `ФОРМАТ поста (тримайся саме його): ${brief.formatName} — ${brief.formatRecipe}`,
    `Тон/голос: ${brief.voice}`,
    `Форма подачі: ${brief.shape}`,
    `Орієнтир для хука (можеш переписати по-своєму): ${brief.hookPattern}`,
    `Довжина: ${brief.lengthHint}`,
    `Кінцівка: живе питання до жінки, БЕЗ продажу й БЕЗ шаблонних фраз.`,
  ];
  if (brief.insight) {
    lines.push("", "ОПОРА (реальний інсайт із ресёрчу — перепиши своїми словами, не копіюй):", brief.insight);
  }
  lines.push(
    "",
    "УНИКАЙ заїжджених кліше: «знайди себе», «полюби себе» без розкриття, «все буде добре», «ти варта кращого» як порожній лозунг, «жінка має право». Шукай свіжі, тілесні, конкретні формулювання.",
  );
  if (recents.length) {
    lines.push(
      "",
      "Ось недавні пости — НЕ повторюй їхні теми, приклади, структуру й кінцівки, будь іншим:",
      ...recents.slice(-10).map((p, i) => `${i + 1}) ${String(p).replace(/\s+/g, " ").slice(0, 160)}`),
    );
  }
  lines.push("", "Згенеруй ОДИН унікальний пост українською строго за схемою відповіді.");

  const resp = await c.models.generateContent({
    model: MODEL,
    contents: lines.join("\n"),
    config: {
      systemInstruction: SYSTEM,
      responseMimeType: "application/json",
      responseSchema: SCHEMA,
      maxOutputTokens: 8192,
      temperature: 1.1,
      topP: 0.95,
    },
  });

  const text = resp.text;
  if (!text) return null;
  const data = JSON.parse(text);
  if (typeof data.post === "string") {
    data.post = data.post.replace(/\\n/g, "\n").trim();
    data.char_count = data.post.length;
  }
  return data;
}
